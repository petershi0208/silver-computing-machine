package com.yqwan.service;

public class Gameinfo {
	
	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return
	 */
	public static int plusnum(int num1,int num2){
		return num1+num2;
	}
	/**
	 * 
	 * @param 被减数
	 * @param 减数
	 * @return
	 */
	public static int minusnum(int num1,int num2){
		return num1-num2;
	}
}
